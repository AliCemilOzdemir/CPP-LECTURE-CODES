#include <iostream>
using namespace std;

int main()
{
    // Example: Increment Operators

    int number1 = 5;
    
    cout << "number1 is " << number1 << endl;
    cout << "number1++ is " << number1++ << endl;
    cout << "++number1 is " << ++number1 << endl;
    cout << "finally, number1 is now " << number1 << endl;

return 0;
}