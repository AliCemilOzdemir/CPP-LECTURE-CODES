#include <iostream>
using namespace std;
int main ()
{
    // Example: Assignment Operators

    int a, b; // a:?, b:?

    a = 10; // a:10, b:?
    b = 4; // a:10, b:4
    cout << "a: " << a << ", b: " << b << endl;

    a = b; // a:4, b:4
    b = b/2; // a:4, b:2
    cout << "a: " << a << ", b: " << b << endl;

return 0;
}
